package org.string;

public class Swapping {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println("before swapping");
		System.out.println("a is "+a);
		System.out.println("b is"+b);
		
		
		int c=b;
		a=b;
		b=c;
		
		
		System.out.println("after swapping");
		System.out.println("a is"+b);
		System.out.println("b is"+b);
		
		
		
		
		
	}

}
